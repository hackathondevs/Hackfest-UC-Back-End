CREATE TABLE Enclosures (
    ID BIGINT UNSIGNED AUTO_INCREMENT,
    
    Name VARCHAR(255) NOT NULL,
    Coordinate POINT NOT NULL,
    
    CreatedAt DATETIME NOT NULL DEFAULT NOW(),
    
    PRIMARY KEY (ID)
) ENGINE = INNODB DEFAULT CHARSET = UTF8;

